        [HttpPost]
        [Authorize(Roles = "Admin")]
        [ValidateAntiForgeryToken]
        public ActionResult Delete(List<SelectItemView> model)
        {
            CheckAda();
            //     var db = new ApplicationDbContext();
            foreach (var umod in model)
            {

                ApplicationUser user = this.ApplicationDbContext.Users.Find(umod.Userid);
                umod.Name = user.UserName;
                //umod.Userid = user.ToString();
                if (user.Active && umod.Checked)
                {
                    foreach (Image image in user.Images.ToArray())
                    {
                        // db.Images.Remove(image);
                        this.ApplicationDbContext.Images.Remove(image);
                    }
                    user.Active = false;
                }
                else if (!user.Active && !umod.Checked)
                {
                    /*
                    *  Reactivate a user
                    */
                    user.Active = true;

                }

            }
            //  db.SaveChanges();
            this.ApplicationDbContext.SaveChanges();

            ViewBag.Message = "Users successfully deactivated /reactivated.";
            /*
            *  Display the same UI with the success message
            */
            return View(model);
        }
